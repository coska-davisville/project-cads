    id SERIAL PRIMARY KEY,
    name VARCHAR(100),
    provinceId INT REFERENCES Province(id)
);

CREATE TABLE "User" (
    id SERIAL PRIMARY KEY,
    passward VARCHAR(255),
    memberType INT DEFAULT 0,
    firstName VARCHAR(100) DEFAULT "",
    lastName VARCHAR(100) DEFAULT "",
    primaryContactNo VARCHAR(100) DEFAULT "",
    secondaryContactNo VARCHAR(100) DEFAULT "",
    email VARCHAR(200),
    streetAddress VARCHAR(255) DEFAULT "",
    extraAddress VARCHAR(255) DEFAULT "",
    cityId INT REFERENCES City(id)
);

